#import "NSString+TechD.h"

//  ------------------------------------------------------------------------------------------------
//  ------------------------------------------------------------------------------------------------

@implementation NSString (TechD)

//  ------------------------------------------------------------------------------------------------
//  ------------------------------------------------------------------------------------------------
- ( BOOL ) isNumeric
{
    NSNumberFormatter             * numberFormatter;
    
    numberFormatter                 = [[NSNumberFormatter alloc] init];
    if ( nil == numberFormatter )
    {
        return NO;
    }
    
    if ( [numberFormatter numberFromString: self] == nil )
    {
        return NO;
    }
    return YES;
}
//  ------------------------------------------------------------------------------------------------
//  ------------------------------------------------------------------------------------------------

@end

